# ===== ГЛОБАЛЬНЫЕ ПЕРЕМЕННЫЕ =====
is_mac = platform.system() == 'Darwin'
paste_key = Key.cmd if is_mac else Key.ctrl
keyboard_controller = Controller()

spam_active = False
spam_interval = 1.0
spam_thread = None

def generate_insult():
    return random.choice(INSULTS)

def spam_worker():
    global spam_active
    while spam_active:
        insult = generate_insult()
        pyperclip.copy(insult)
        with keyboard_controller.pressed(paste_key):
            keyboard_controller.press('v')
            keyboard_controller.release('v')
        keyboard_controller.press(Key.enter)
        keyboard_controller.release(Key.enter)
        print(f"[СПАМ] {insult}")
        time.sleep(spam_interval)

def on_press(key):
    try:
        if key.char == '7':
            insult = generate_insult()
            pyperclip.copy(insult)
            with keyboard_controller.pressed(paste_key):
                keyboard_controller.press('v')
                keyboard_controller.release('v')
            keyboard_controller.press(Key.enter)
            keyboard_controller.release(Key.enter)
            print(f"[+] {insult}")
    except AttributeError:
        pass

def on_release(key):
    if key == Key.esc:
        return False

def console_listener():
    global spam_active, spam_interval, spam_thread
    print("""
╔═══════════════════════════════════════════════════════════════════╗
║   INSULT GENERATOR v8.0 (КОРОТКИЕ ФРАЗЫ)                       ║
║   Команды:                                                      ║
║   спам X   – начать спам со скоростью X сообщений/сек          ║
║   стоп     – остановить спам                                    ║
║   скорость X – установить скорость (сообщений/сек)              ║
║   7        – одиночная отправка (всегда работает)               ║
║   ESC      – выход                                              ║
╚═══════════════════════════════════════════════════════════════════╝
    """)
    while True:
        cmd = input("> ").strip().lower()
        if cmd.startswith('спам'):
            parts = cmd.split()
            if len(parts) == 2:
                try:
                    speed = float(parts[1])
                    if speed <= 0:
                        print("[!] Скорость должна быть больше 0.")
                        continue
                    spam_interval = 1.0 / speed
                    if spam_active:
                        print("[!] Спам уже запущен. Остановите его командой 'стоп'.")
                        continue
                    spam_active = True
                    spam_thread = threading.Thread(target=spam_worker, daemon=True)
                    spam_thread.start()
                    print(f"[+] Спам запущен. Скорость: {speed} сообщений/сек.")
                except ValueError:
                    print("[!] Использование: спам X, где X - число сообщений в секунду.")
            else:
                print("[!] Использование: спам X, где X - число сообщений в секунду.")
        elif cmd == 'стоп':
            if spam_active:
                spam_active = False
                if spam_thread:
                    spam_thread.join(timeout=0.5)
                print("[+] Спам остановлен.")
            else:
                print("[!] Спам не запущен.")
        elif cmd.startswith('скорость'):
            parts = cmd.split()
            if len(parts) == 2:
                try:
                    speed = float(parts[1])
                    if speed <= 0:
                        print("[!] Скорость должна быть больше 0.")
                        continue
                    spam_interval = 1.0 / speed
                    print(f"[+] Скорость установлена: {speed} сообщений/сек.")
                    if spam_active:
                        print("[!] Спам активен, новые настройки применятся при следующем запуске спама.")
                except ValueError:
                    print("[!] Использование: скорость X, где X - число сообщений в секунду.")
            else:
                print(f"[*] Текущая скорость: {1.0/spam_interval:.2f} сообщений/сек.")
        elif cmd == 'выход':
            if spam_active:
                spam_active = False
                if spam_thread:
                    spam_thread.join(timeout=0.5)
            print("[+] Выход.")
            sys.exit(0)
        else:
            print("[!] Неизвестная команда. Доступны: спам X, стоп, скорость X, выход.")

def main():
    listener = keyboard.Listener(on_press=on_press, on_release=on_release)
    listener.daemon = True
    listener.start()
    console_listener()

if __name__ == "__main__":
    try:
        main()
    except KeyboardInterrupt:
        print("\n[!] Программа завершена.")
    except Exception as e:
        print(f"[-] Ошибка: {e}")
    finally:
        input("Нажмите Enter для выхода...")
